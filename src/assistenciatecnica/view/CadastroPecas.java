    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botaoSalvarActionPerformed
    {//GEN-HEADEREND:event_botaoSalvarActionPerformed
		if(this.validaCampos())
		{
			controladorBD				= new ControladorBancodeDados();
			controlador				= new ControladorPeca();

			if(this.textoCodigo.getText().equals("** NOVO **"))
				this.peca = new Pecas();
			else
				this.peca = controlador.getPecaByCodigo(Long.parseLong(this.textoCodigo.getText()), this);

			//Seta os valores
			this.peca.setPeca(this.textoPeca.getText());
			this.peca.setValor(this.textoValor.getValue());
			this.peca.setQuantidade(Integer.parseInt(this.textoQuantidade.getText()));
			this.peca.setObservacao(this.textoObs.getText());
			
			//Fornecedores
			if(tabelaFornecedor.isEditing())
				tabelaFornecedor.getCellEditor().stopCellEditing();
			List<Fornecedores> fornecedores = new ArrayList<Fornecedores>();
			for(int i=0; i<tabelaFornecedor.getRowCount(); i++)
			{
				Fornecedores fornecedor	= new Fornecedores();
				fornecedor = new ControladorFornecedores().getFornecedorByCodigo(Long.parseLong(tabelaFornecedor.getValueAt(i, 0).toString()), this);
				
				fornecedores.add(fornecedor);
			}
			this.peca.setFornecedores(fornecedores);

			//Atualiza Banco
			if(this.textoCodigo.getText().equals("** NOVO **"))
				controladorBD.cadastrar(this.peca, (JFrame)this);
			else
				controladorBD.atualizar(this.peca, (JFrame)this);

			this.limpaTela();
			this.habilitaCampos(true);
			this.botaoAlterar.setEnabled(false);
			this.botaoApagar.setEnabled(false);

			controladorBD				= null;
			controlador				= null;
		}
    }//GEN-LAST:event_botaoSalvarActionPerformed

