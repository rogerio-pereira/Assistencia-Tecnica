	/*
	 * Método adicionaFornecedor
	 * Adiciona o fornecedor na tabela
	 */
	public Boolean adicionaFornecedor(String codigo, String fornecedor)
	{
		try
		{
			Boolean insere = true;

			for(int i=0; i<tabelaFornecedor.getRowCount(); i++)
			{
				if(tabelaFornecedor.getValueAt(i, 0).toString().equals(codigo))
					insere = false;
			}

			if(insere)
			{
				DefaultTableModel modelo = (DefaultTableModel) tabelaFornecedor.getModel();
				modelo.addRow(new String[]	{
											codigo,
											fornecedor
										});
			}

			return true;
		}
		catch (Exception e)
		{
			return false;
		}
	}
	
	/*
	 * Método getServicoByCodigo
	 * Obtém o servico de acordo com o codigo
	 */
	public void getPecaByCodigo(Long codigo)
	{
		this.controlador	= new ControladorPeca();
		this.peca			= this.controlador.getPecaByCodigo(codigo, this);
		
		//Seta os valores
		this.textoCodigo.setText(this.peca.getCodigo().toString());
		this.textoPeca.setText(this.peca.getPeca());
		this.textoValor.setText(this.peca.getValor());
		this.textoQuantidade.setText(String.valueOf(this.peca.getQuantidade()));
		this.textoObs.setText(this.peca.getObservacao());
		
		for(Fornecedores fornecedor: this.peca.getFornecedores())
		{
			this.adicionaFornecedor(fornecedor.getCodigo().toString(), fornecedor.getNome());
		}
		
		this.habilitaCampos(false);
		
		botaoApagar.setEnabled(true);
		botaoAlterar.setEnabled(true);
	}
			


